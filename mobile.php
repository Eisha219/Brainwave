  playPauseBtn.addEventListener('click', () => {
    if (video.paused) {
      video.play();
      playPauseBtn.innerHTML = '<div class="icon pause"></div>';
    } else {
      video.pause();
      playPauseBtn.innerHTML = '<div class="icon play"></div>';
    }
  });

  video.addEventListener('play', () => {
    playPauseBtn.innerHTML = '<div class="icon pause"></div>';
  });

